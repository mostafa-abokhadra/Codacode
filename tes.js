#!/usr/bin/node
const currentDate = new Date();

console.log(currentDate.getFullYear()); // e.g., 2025
console.log(currentDate.getMonth());    // e.g., 0 (January, 0-based)
console.log(currentDate.getDate());     // e.g., 27
console.log(currentDate.getHours());    // e.g., 14
console.log(currentDate.getMinutes());  // e.g., 34
console.log(currentDate.getSeconds());  // e.g., 56
console.log(currentDate.getDay());      // e.g., 1 (Monday, 0 = Sunday)
